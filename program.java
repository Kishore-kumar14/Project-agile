public class SimpleMultiplication {
    public static void main(String[] args) {
        // Declare two numbers (operands)
        int number1 = 15;
        int number2 = 6;

        // Perform multiplication using the '*' operator
        int product = number1 * number2;

        // Print the result
        System.out.println("The product is: " + product); // Output: The product is: 90
    }
}
